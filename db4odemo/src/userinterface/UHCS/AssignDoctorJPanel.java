    public void populateTable() {
        DefaultTableModel md=(DefaultTableModel)tblDoctorList.getModel();
        md.setRowCount(0);
        Object row[]= new Object[3];
        
        //taking network, enterprise level doctor list
        ArrayList<Doctor> doctorList  = system.getDoctorDirectory().getDoctorList();
        for (Doctor doctor : doctorList) {
            row[0] = doctor; //username gets printed
            row[1] = doctor.getEmployee().getName();
            row[2] = doctor.getEmployee().isIsFree();  //true false
                             
            md.addRow(row);
        }
    }
    
