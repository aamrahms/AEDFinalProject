    private void btnAssignActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignActionPerformed
        int selectedRow = tblDoctorList.getSelectedRow();
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null,"Please select a doctor from the table to assign","Warning",JOptionPane.WARNING_MESSAGE);
        }
        else
        {
            Doctor doctor  = (Doctor)tblDoctorList.getValueAt(selectedRow, 0);
        boolean isFree = (boolean) tblDoctorList.getValueAt(selectedRow, 2);
            
            if(!isFree) {
                JOptionPane.showMessageDialog(null,"Sorry selected doctor is not free, select another doctor who is free","Warning",JOptionPane.WARNING_MESSAGE);
                return;
            }
            
            //Set status of complaint 
            complaintWorkRequest.setStatus("UHCS Doctor Assigned");
            
            //set DoctorName on complaint
            //complaintWorkRequest.setDoctor(doctor.getEmployee().getUsername());
            
            //assign it in enterprise level doctor object
            doctor.setComplaint(complaintWorkRequest);
            //doctor.getCompletedComplaintsList().add(complaintWorkRequest);
            //make doctor busy
            doctor.getEmployee().setIsFree(false);
            
            //assign it in userAccount level of doctor (to do on signup)
            UserAccount doctorUserAccount = system.getUserAccountDirectory().fetchUaWithUserName(doctor.getEmployee().getUsername());
            doctorUserAccount.getWorkQueue().getComplaintList().add(complaintWorkRequest);
            //make ua busy
            doctorUserAccount.getEmployee().setIsFree(false);
            
            JOptionPane.showMessageDialog(null,"Complaint with id: "+ complaintWorkRequest.getComplaintID() +"is assigned to Doctor "+doctor.getEmployee().getName());
                    
            // clear table
            DefaultTableModel model = (DefaultTableModel) tblDoctorList.getModel();
            model.setRowCount(0);
        
            //back page
            userProcessContainer.remove(this);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.previous(userProcessContainer);

        }
    }//GEN-LAST:event_btnAssignActionPerformed

